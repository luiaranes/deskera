function trim_excerpt($_content) {
$_max_chars = 250;

$_content = strip_tags($_content);
$_content = html_entity_decode($_content, ENT_COMPAT, 'UTF-8'); // decode html entities to characters with (charset UTF-8)
if(strlen($_content) > $_max_chars) {
	$_content = substr($_content, 0, $_max_chars);	
}
